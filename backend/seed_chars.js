async function seed() {
  await initDB();
  const db = getDB();

  const insert = db.prepare('INSERT OR IGNORE INTO characters (char, pinyin, strokes, category, words, sentence) VALUES (?, ?, ?, ?, ?, ?)');

  let total = 0;
  const allInserted = {};

  function insertBatch(chars, category) {
    chars.forEach(([ch, py, st]) => {
      try {
        insert.run([ch, py, st, category, getWords(ch), getSentence(ch)]);
        if (!allInserted[ch]) { total++; allInserted[ch] = true; }
      } catch(e) {}
    });
  }

  console.log('插入学前汉字...');
  insertBatch(PRESCHOOL, 'preschool');
  console.log('插入一年级上册...');
  insertBatch(GRADE1_UP, 'grade1_up');
  console.log('插入一年级下册...');
  insertBatch(GRADE1_DOWN, 'grade1_down');
  insert.free();

  // Count
  const stmt = db.prepare('SELECT category, COUNT(*) as cnt FROM characters GROUP BY category ORDER BY category');
  const cats = [];
  while (stmt.step()) cats.push(stmt.getAsObject());
  stmt.free();

  const totalStmt = db.prepare('SELECT COUNT(*) as cnt FROM characters');
  totalStmt.step();
  const dbCount = totalStmt.getAsObject().cnt;
  totalStmt.free();

  console.log('\n=== 完成 ===');
  console.log('新插入唯一汉字:', total, '个');
  cats.forEach(c => console.log('  ' + c.category + ': ' + c.cnt + ' 字'));
  console.log('数据库总计:', dbCount, '个汉字');
  require('./db').saveDB();
  process.exit(0);
}

seed().catch(e => { console.error('ERROR:', e.message || e); process.exit(1); });
